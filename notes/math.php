<?php
    $num1 = 10;
    $num2 = 20;

    // Addition
    $sum = $num1 + $num2;
    echo "Sum: " . $sum . "<br>";

    // Subtraction
    $diff = $num1 - $num2;
    echo "Difference: " . $diff . "<br>";

    // Multiplication
    $product = $num1 * $num2;
    echo "Product: " . $product . "<br>";

    // Division
    $quotient = $num1 / $num2;
    echo "Quotient: " . $quotient . "<br>";

    // Modulus
    $modulus = $num1 % $num2;
    echo "Modulus: " . $modulus . "<br>";
?>